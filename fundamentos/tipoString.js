const escola = "Cod3r"

console.log(escola.charAt(2)) // o metodo charAt() ele restorna o indicie pedido.

console.log(escola.indexOf("d")) // o metodo indexOf() voce passa o valor e ele retorna em qual indice esta.

console.log(escola.substring(2)) // o metodo substring() ele retorna o resto a partir o indice indicado
console.log(escola.substring(0, 4))

console.log('Escola '.concat(escola).concat("!")) // o metodo concat() ele concatena strings

console.log(escola.replace(3, "e")) // o metodo replace() ele troca o valor passado pelo o novo valor atualizado.

console.log('Ana,Maria,Pedro'.split(',')) // o metodo spli() transforma a string em um array.